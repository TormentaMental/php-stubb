(function () {
    angular.module('app').directive('sessionDropdown', ['config',
        function (config) {
            
            return {
                restrict: 'EA',
                templateUrl: config.SRC_FOLDER + '/common/templates/session-dropdown.html',   
                scope: {
                    data: "="
                },
                controller: ['$scope', function ($scope) {
                        
                    $scope.user = $scope.data;

                    $scope.translations = {
                        update_info: 'Update info',
                        logout: 'Logout',
                    }
                }]
            };
        }
    ]);       
})();